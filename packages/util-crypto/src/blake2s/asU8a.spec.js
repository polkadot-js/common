const { blake2sAsU8a } = require('./index');

describe('blake2sAsU8a', () => {
  it('creates the correct hash', () => {
    expect(
      blake2sAsU8a('abc')
    ).toEqual(
      Uint8Array.from([
        0x50, 0x8c, 0x5e, 0x8c, 0x32, 0x7c, 0x14, 0xe2, 0xe1, 0xa7, 0x2b, 0xa3, 0x4e, 0xeb, 0x45, 0x2f, 0x37, 0x45, 0x8b, 0x20, 0x9e, 0xd6, 0x3a, 0x29, 0x4d, 0x99, 0x9b, 0x4c, 0x86, 0x67, 0x59, 0x82
      ])
    );
  });
});
